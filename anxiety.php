                <h2 class="h-bold" align="center"><font color="#3FBBC0">What is Anxeity?</h2></font>
                <p class="aboutp"> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Anxiety is the mind and body’s reaction to stressful, dangerous, or unfamiliar situations. It’s the sense of uneasiness, distress, or dread you feel before a significant event.  A certain level of anxiety helps us stay alert and aware, but for those suffering from an anxiety disorder, it feels far from normal – it can be completely debilitating. Over the years, you might have struggled with feelings of intense worry or fear over what might happen. Whether it is the first day of college/office or talking to unknown people, the fearful thoughts might have troubled you and brought multiple sleepless nights.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;It’s normal to feel anxious for major life-events but when this state becomes part of your personality and interferes with your daily functioning, it needs to be healed.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Experiencing anxiety is defined as a state of prolonged fear or arousal in response to a threat that is either ambiguous or unspecific. It is a reaction to the situation or context in which threats may take varied forms and predictions which further impacts your well-being. In such scenarios, anxiety needs to be conquered before it ruins your health and happiness</p>
              </div>
        <div class="section-heading text-center">
                <h2 class="h-bold" align="center"><font color="#3FBBC0">What are Anxiety Disorders?</h2></font>
                <p class="aboutp">  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Anxiety disorders are characterized by a general feature of excessive fear (i.e. emotional response to perceived or real threat) and/or anxiety (i.e. worrying about a future threat) and can have negative behavioral and emotional consequences.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The purpose of online counselling and therapy for anxiety attacks and anxiety disorders is not only to help you overcome your state of prolonged fear but also become capable of expressing yourself in front of others. We understand that it is sometimes natural to feel anxious in certain situations, but let us tell you how to redirect your feelings of worry into calmness that serves as a pathway to achieve success and happiness in your life.</p>
              </div>     
        <div class="section-heading text-center">
                <h2 class="h-bold" align="center"><font color="#3FBBC0">Types of Anxiety Disorders:</h2></font>
                <p class="aboutp"> Anxiety disorder is an umbrella term that includes different conditions:
                	<ul class="about" style="font-size: 18px; text-align: left; color: solid black;">
                		<li><b>Panic disorder :</b>&nbsp;&nbsp;&nbsp; You feel terror that strikes at random. During a panic attack, you may also sweat, have chest pain, and Feel palpitations(unusually strong or irregular heartbeats). Sometimes you may feel like you’re choking or having a heart attack.</li>
                		<li><b>Social anxiety disorder :</b>&nbsp;&nbsp;&nbsp; Also called social phobia, this is when you feel overwhelming worry and self-consciousness about everyday social situations. You fixate about others judging you or on being embarrassed or ridiculed.</li>
                		<li><b>Specific phobias :</b> &nbsp;&nbsp;&nbsp;You feel intense fear of a specific object or situation, such as heights or flying. The fear goes beyond what’s appropriate and may cause you to avoid ordinary situations.</li>
                		<li><b>Generalized anxiety disorder:</b>&nbsp;&nbsp;&nbsp;You feel excessive, unrealistic worry and tension with little or no reason.</li>
                	</ul>
                </p>
              </div> 
                    
      <div class="section-heading text-center">
         <h2 class="h-bold" align="center"><font color="#3FBBC0">Causes of Anxiety:</h2></font>
          <p class="aboutp"> People tend to stress about anything and everything in life. They don’t seem to have specific cause for their worry it can be anything, which gradually starts affecting their physical and emotional health tremendously.
                  <ul class="about" style="font-size: 18px; text-align: left; color: solid black;">
                    <li><b>Traumatic experience or exposure: </b>&nbsp;&nbsp;&nbsp; Tragic experience or exposure often imprints us with unpleasant and fearful memories which does not allow us to think rationally or practically. For example witnessing an accident, exposure to unpleasant sight, abuse.</li>
                    <li><b>Future uncertainty or negative anticipation: </b>&nbsp;&nbsp;&nbsp; Excessive and stressing in a negative way about future like finance, security, stability, loneliness etc. may lead to anxiety.</li>
                     <li><b>Childhood trauma: </b>&nbsp;&nbsp;&nbsp;Childhood trauma has a significant impact in the overall shaping of cognitive and emotional makeup of a person, which later on become a major cause of mental health concerns and worries in adulthood.</li>
                     <li><b>Disturbed Sleeping and Eating Habits: </b>&nbsp;&nbsp;&nbsp; Unhealthy sleeping and eating routine may make you prone towards the anxiety over a period of time.</li>
                     <li><b>Medical or medicine induced Anxiety: </b>&nbsp;&nbsp;&nbsp; Chronic illness or serious health condition may lead to excessive worry in terms of finances, health risk, long term medical dependency, side-effects of medicines.</li>
                     <li><b>Work Stress: </b>&nbsp;&nbsp;&nbsp; Work life has become one of the major stress creating aspect of everyone’s life, it not only affects the working person but their family due to unavailability, lack of quality time. Prolonged work hours, demanding job, pressure and exhaustion can take an adverse toll on their life.</li>
                     <li><b>Bullying and Harassment: </b>&nbsp;&nbsp;&nbsp; Bullying is prevalent in various organization be it school, college or workplace in different forms. Taking undue advantage, taking employees for granted, constant demotivation and negativity, inter and intra politics becomes a reason for bullying and abuse in various forms. This may result in anxious and restless tendencies in employees.</li>
                  </ul>
                </p>
