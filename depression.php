                <h2 class="h-bold" align="center"><font color="#3FBBC0">Characteristics And Causes Of Depression</h2></font>
                <p class="aboutp"> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Depression is probably the most common psychiatric complaint and has been described by physicians since before the time of ancient Greek physician Hippocrates, who called it melancholia. The course of the disorder is extremely variable from person to person; it may be mild or severe, acute or chronic. Untreated, depression may last an average of four months or longer. Depression is twice as prevalent in women than in men. The typical age of onset is in the 20s, but it may occur at any age.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                Depression can have many causes. Unfavourable life events can increase a person’s vulnerability to depression or trigger a depressive episode. Negative thoughts about oneself and the world are also important in producing and maintaining depressive symptoms. However, both psychosocial and biochemical mechanisms seem to be important causes; the chief biochemical cause appears to be the defective regulation of the release of one or more naturally occurring neurotransmitters in the brain, particularly norepinephrine and serotonin. Reduced quantities or reduced activity of these chemicals in the brain is thought to cause the depressed mood in some sufferers.</p>
