                <h2 class="h-bold" align="center"><font color="#3FBBC0">Positive v/s Negative Emotions</h2></font>
                <p class="aboutp"> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Emotions (feelings) are a normal and important part of our lives.  Some emotions are positive. Think of happiness, joy, interest, curiosity, excitement, gratitude, love, and contentment. These positive emotions feel good. Negative emotions — like sadness, anger, loneliness, jealousy, self-criticism, fear, or rejection — can be difficult, even painful at times.  That's especially true when we feel a negative emotion too often, too strongly, or we dwell on it too long.  Negative emotions are impossible to avoid, though. Everyone feels them from time to time. They may be difficult, but we can learn to handle them.  Here are three steps that can help you handle negative emotions.</p>
              </div>
        <div class="section-heading text-center">
                <h2 class="h-bold" align="center"><font color="#3FBBC0">Step 1: Identify the Emotion</h2></font>
                <p class="aboutp">  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Learning to notice and identify your feelings takes practice. In addition to focusing on your feelings, check in with your body, too. You may feel body sensations with certain emotions — perhaps your face gets hot, for example, or your muscles tense<br>   <ul class="about" style="font-size: 18px; text-align: left; color: solid black;">
                    <li><b>Be aware of how you feel :</b>&nbsp;&nbsp;&nbsp; When you have a negative emotion, such as anger, try to name what you're feeling.  For example:That guy Ian in my study group makes me so mad!
I get so jealous when I see that girl/guy with my ex.
I feel afraid whenever I have to walk past those bullies.</li>
                    <li><b>Don't hide how you feel from yourself: </b>&nbsp;&nbsp;&nbsp; You might not want to broadcast your feelings to other people (like your ex, for example, or that guy in your study group who is making you mad). But don't suppress your feelings entirely. Simply naming the feeling is a lot better than pretending not to have it — or exploding without thinking.</li>
                    <li><b>Know why you feel the way you do:</b> &nbsp;&nbsp;&nbsp;Figure out what happened that got you feeling the way you do.</li>
                    <li><b>Don't blame: </b>&nbsp;&nbsp;&nbsp;Being able to recognize and explain your emotions isn't the same as blaming someone or something for the way you feel. Your ex probably isn't seeing someone new as a way to get back at you, and the guy who takes credit for your work might not even realize what he is doing. How you feel when these things happen comes from inside you. Your feelings are there for a reason — to help you make sense of what's going on.</li>
                      <li><b>Accept all your emotions as natural and understandable: </b>&nbsp;&nbsp;&nbsp;Don't judge yourself for the emotions you feel. It's normal to feel them. Acknowledging how you feel can help you move on, so don't be hard on yourself.</li>
                  </ul>
                </p>
              </div>     
              <div class="section-heading text-center">
                <h2 class="h-bold" align="center"><font color="#3FBBC0">Step 2: Take Action</h2></font>
                <p class="aboutp">  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Once you've processed what you're feeling, you can decide if you need to express your emotion. Sometimes it's enough to just realize how you feel, but other times you'll want to do something to feel better.<br>   <ul class="about" style="font-size: 18px; text-align: left; color: solid black;">
                    <li><b>Think about the best way to express your emotion: </b>&nbsp;&nbsp;&nbsp; Is this a time when you need to gently confront someone else? Talk over what you're feeling with a friend? Or work off the feeling by going for a run?</li>
                    <li><b>Learn how to change your mood: </b>&nbsp;&nbsp;&nbsp;At a certain point, you'll want to shift from a negative mood into a positive one. Otherwise your thinking may get stuck on how bad things are, and that can drag you down into feeling worse. Try doing things that make you happy, even if you don't feel like it at the time. For example, you might not be in the mood to go out after a breakup, but going for a walk or watching a funny movie with friends can lift you out of that negative space.</li>
                    <li><b>Know why you feel the way you do:</b> &nbsp;&nbsp;&nbsp;Figure out what happened that got you feeling the way you do.</li>
                  
                  </ul>
                </p>
              </div>     
        <div class="section-heading text-center">
                <h2 class="h-bold" align="center"><font color="#3FBBC0">Step 3: Get Help With Difficult Emotions:></font>
                <p class="aboutp"> Sometimes, no matter what you do, you can't shake a tough emotion. If you find yourself stuck in feelings of sadness or worry for more than a couple of weeks, or if you feel so upset that you think you might hurt yourself or other people, you may need extra help.  Talk to a school counselor, parent, trusted adult, or therapist. Counselors and therapists are trained to teach people how to break out of negative emotions. They can provide lots of tips and ideas that will help you feel better.
                </p>
