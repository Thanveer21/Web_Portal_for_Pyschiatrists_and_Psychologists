                <h2 class="h-bold" align="center"><font color="#3FBBC0">Types of Sleep Disorders:</h2></font>
                <p class="aboutp">There are several types of sleep disorders depending on the symptoms of the individual. Some common types of sleep disorders are:
                	<ul class="about" style="font-size: 18px; text-align: left; color: solid black;">
                		<li><b>Insomnia:</b>&nbsp;&nbsp;&nbsp;It is an inability to fall asleep or get restful sleep. It can be triggered by stress and anxiety, health problems, side effects of medications, other sleep disorders.</li>
                		<li><b>Sleep bruxism:</b>&nbsp;&nbsp;&nbsp;  It is a sleep disorder in which you clench or grind your teeth. This can occur infrequently and results in headaches, flattened or chipped teeth, and jaw problems such as tightness, soreness, and difficulties chewing.</li>
                		<li><b>Narcolepsy:</b> &nbsp;&nbsp;&nbsp;It is a sleep disorder characterized by “sleep attacks” in the mid of sleeping or period of wakefulness, often caused by a strong emotional reactions to something. This problem is caused by dysfunction that disturbs the brain’s normal control over waking and sleeping.
<li><b>REM Sleep Behaviour Disorder (RBD):</b> This is a type of sleep disorder where a person enacts their dreams while in REM sleep. RBD predominantly affects older adults, and the associated behaviours that are often violent in nature resulting in stress and anxiety.</li>
                		<li><b>Night terrors:</b>&nbsp;&nbsp;&nbsp;These are not simply nightmares or bad dreams. It is a state where the person often does a loud shout or scream, though the sleeper may sit up in bed and may appear awake and terrified.</li>
                	</ul>
                </p>
